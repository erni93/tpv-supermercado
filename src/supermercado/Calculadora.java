    int posicionActual_historial = 0;
    Operacion[] operaciones_historial = new Operacion[3];
    private void anadirHistorial(Operacion registrarOperacion){
        txa_historial.setText("");
        operaciones_historial[posicionActual_historial] = registrarOperacion;
        for (int i=0; i<operaciones_historial.length; i++){
            if (operaciones_historial[i] != null){
                if (i == operaciones_historial.length-1){
                    txa_historial.setText(txa_historial.getText() + operaciones_historial[i].toString());
                }else{
                    txa_historial.setText(txa_historial.getText() + operaciones_historial[i].toString()+ "\n");
                }
            }
        }
        if (posicionActual_historial == 2){
            posicionActual_historial = 0;
            for (int i=1; i<operaciones_historial.length; i++){
                operaciones_historial[i] = null;
            }
        }else{
            posicionActual_historial++;
        }
    }
    private void resetearValores(){
        comaInsertada = false;
        primerDigitoCero = false;
        esNegativo = false;
    }
