    private void bt_borrarUnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_borrarUnoActionPerformed
        if (txf_resultado.getText().length() >= 2){
            char caracterAborrar = txf_resultado.getText().charAt( (txf_resultado.getText().length()-1) );
            if(caracterAborrar == ',' && esNegativo && primerDigitoCero){
                bt_cambiarSignoActionPerformed(evt);
            }
            System.out.println("Valor de primerDigitoCero: " + primerDigitoCero);
            System.out.println("Valor de comaInsertada: " + comaInsertada);
            if(caracterAborrar == ',' && primerDigitoCero){
                txf_resultado.setText("");
                resetearValores();
            }else{
                txf_resultado.setText(txf_resultado.getText().substring(0, txf_resultado.getText().length()-1));
            }
            if(caracterAborrar == ','){
                comaInsertada = false;
            }
        }else{
            txf_resultado.setText("");
            resetearValores();
        }
    }//GEN-LAST:event_bt_borrarUnoActionPerformed

    private void bt_cambiarSignoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cambiarSignoActionPerformed
        if (txf_resultado.getText().length() >=1){
            if (txf_resultado.getText().charAt(0) != '0' || comaInsertada){
                if (esNegativo && txf_resultado.getText().length() >=2){
                    //Si ya es negativo se le quita el signo
                    txf_resultado.setText(txf_resultado.getText().substring(1));
                    esNegativo = false;
                }else{
                    txf_resultado.setText("-" + txf_resultado.getText());
                    esNegativo = true;
                }
            }  
        }
    }//GEN-LAST:event_bt_cambiarSignoActionPerformed

