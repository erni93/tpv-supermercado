    private void bt_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_igualActionPerformed
        if (numeroAux.equals("") == false){
            numeroAux = numeroAux.replace(',', '.');
            txf_resultado.setText(txf_resultado.getText().replace(',', '.'));
            Operacion nuevaOperacion = new Operacion(Double.parseDouble(numeroAux), Double.parseDouble(txf_resultado.getText()), tipoOperacion);
            label_primerNumero.setText(nuevaOperacion+ "");
            String resultado = "" + nuevaOperacion.getResultado();
            txf_resultado.setText(resultado.replace('.', ','));
            numeroAux = "";
            anadirHistorial(nuevaOperacion);
        }else{
           txf_resultado.setText(txf_resultado.getText().replace(',', '.'));
           Operacion nuevaOperacion = new Operacion(Double.parseDouble(txf_resultado.getText()), tipoOperacion);
           label_primerNumero.setText(nuevaOperacion+ "");
           String resultado = "" + nuevaOperacion.getResultado();
           txf_resultado.setText(resultado.replace('.', ','));
           anadirHistorial(nuevaOperacion);
        }
    }//GEN-LAST:event_bt_igualActionPerformed

